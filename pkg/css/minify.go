// Derive css code based on a HTML doc using Tachyons v4.9.1
func Derive(doc io.Reader) string {
	all := []string{}
	min := []string{}
	mid := []string{}
	max := []string{}

	htmldoc, err := html.Parse(doc)
	if err != nil {
		panic(err)
	}

	for _, stl := range basicStyles {
		if stl.required {
			all = append(all, stl.body)
			continue
		}
		c, err := cascadia.Compile(stl.selector)
		if err != nil {
			fmt.Println(stl)
			panic(err)
		}

		if len(c.MatchAll(htmldoc)) > 0 {
			all = append(all, stl.body)
		}
	}

	for _, stl := range minStyles {
		if stl.required {
			min = append(min, stl.body)
			continue
		}
		c, err := cascadia.Compile(stl.selector)
		if err != nil {
			fmt.Println(stl)
			panic(err)
		}

		if len(c.MatchAll(htmldoc)) > 0 {
			min = append(min, stl.body)
		}
	}

	if len(min) > 0 {
		all = append(all, `@media screen and (min-width:30em){`)
		all = append(all, min...)
		all = append(all, `}`)
	}

	for _, stl := range midStyles {
		if stl.required {
			mid = append(mid, stl.body)
			continue
		}
		c, err := cascadia.Compile(stl.selector)
		if err != nil {
			fmt.Println(stl)
			panic(err)
		}

		if len(c.MatchAll(htmldoc)) > 0 {
			mid = append(mid, stl.body)
		}
	}

	if len(mid) > 0 {
		all = append(all, `@media screen and (min-width:30em) and (max-width:60em){`)
		all = append(all, mid...)
		all = append(all, `}`)
	}

	for _, stl := range maxStyles {
		if stl.required {
			max = append(max, stl.body)
			continue
		}
		c, err := cascadia.Compile(stl.selector)
		if err != nil {
			fmt.Println(stl)
			panic(err)
		}

		if len(c.MatchAll(htmldoc)) > 0 {
			max = append(max, stl.body)
		}
	}

	if len(max) > 0 {
		all = append(all, `@media screen and (min-width:60em){`)
		all = append(all, max...)
		all = append(all, `}`)
	}

	strStyles := strings.Join(all, "\n")

	m := minify.New()
	m.AddFunc("text/css", css.Minify)
	strStyles, err = m.String("text/css", strStyles)
	if err != nil {
		panic(err)
	}

	return strStyles
}

